int currentDay = DateTime.now().weekday;

decreaseDay() {
  if (currentDay == 1) {
    currentDay = 7;
  } else {
    currentDay--;
  }
}

increaseDay() {
  if (currentDay == 7) {
    currentDay = 1;
  } else {
    currentDay++;
  }
}
